public class Examplequestion3 {
    public static void main(String[] args) {
        int menu = 3;
        switch (menu) {
            case 1:
                System.out.println("Black coffee");
                break;
            case 2:
                System.out.println("Cold coffee");
                break;
            case 3:
                System.out.println("Hot coffee");
                break;
            case 4:
                System.out.println("Iced coffee");
                break;
            default:
                System.out.println("Invalid choice");
        }
    }
}